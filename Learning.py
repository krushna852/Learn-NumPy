import numpy as np

#Numpy Data Types and Type Casting

a=np.array([1,23,5,6,89])
print(a)
print(type(a))
print(type(a[2]))

a=np.array([23,12.26,47.02345,3.0254,50])
print(a)
print(type(a[1]))

a=np.array([True,False])
print(a)
print(type(a[1]))

a=np.array(["hfhf","apple","choclate","mango"])
print(a)
print(type(a[1]))

a=np.array([56,"hdvdb",56.12,True,"455"])
