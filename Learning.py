#Arthemetic Operations on  1D Array

import numpy as np

a=np.array([1,2,3])  # a1 a2 a3
b=np.array([4,5,6])  # b1 b2 b3

#Addition -> a1+b1 a2+b2 a3+b3
print(a+b)

#Subtraction -> a1-b1 a2-b2 a3-b3
print(a-b)

#Division -> a1/b1 a2/b2 a3/b3
print(a/b)

#Multiplycation -> a1*b1 a2*b2 a3*b3
print(a*b)

#Floor Divison 
print(b//a)

#Modulus
print(b%a)

#power
print(a**2)